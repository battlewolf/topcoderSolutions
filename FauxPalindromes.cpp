class FauxPalindromes {
public:
	string classifyIt(string);
	bool checkPal(string);
};
bool FauxPalindromes::checkPal(string str)
{
	for(int i = 0 ; i < str.size()/2 ; i++)
		if(str[i] != str[str.size()-1-i])
			return false;
	return true;
	
}
string FauxPalindromes::classifyIt(string word) {
	if(checkPal(word)) return "PALINDROME";
	else
	{
		string str = ""; str = str + word[0];
		for(int i = 1 ; i < word.size();i++)
		{
			if(word[i]!=word[i-1]) str = str + word[i];
		}
		if(checkPal(str)) return "FAUX";
	}
	return "NOT EVEN FAUX";
}



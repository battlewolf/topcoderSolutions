class Chessboard {
public:
	string changeNotation(string);
};

string Chessboard::changeNotation(string cell) {
	map<string,int> M1;
	map<int,string> M2;
	
	string var1, var2, var3;
	int summator = 1;
	
	for(int i = 1 ; i <= 8 ; i++)
	{
		var1 = "a";
		for(int j = 1 ; j <= 8 ; j++)
		{
			M2[summator] = var1 + (char)(48+i);
			M1[var1 + (char)(48+i)] = summator;
			var1[0]++;
			summator++;
		}
	}
	if(cell[0]>='a')
	{
		int num = M1[cell];
		string s = "";
		s = s + (char)((num/10) + 48) + (char)( (num%10)+48);
		if(s[0]=='0')s.erase(s.begin());
		return s;
	}
	else
	{
		int num = atoi(cell.c_str());
		return M2[num];
	}
	
	
	
}



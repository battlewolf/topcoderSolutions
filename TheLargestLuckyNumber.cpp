class TheLargestLuckyNumber {
public:
	int find(int);
};

int TheLargestLuckyNumber::find(int n) {
	
	int num = n,c=0;
	while(num)
	{
		c++;
		num=num/10;
	}
	int mul = 1,num1=0, num2 = 0;
	
	for(int i = 1 ; i <= c ; i++)
	{
		
		if(i%2==0){
		num1 = num1 +7 * mul;
		num2 = num2 + 4 * mul;
		}
		else
		{
			num1 = num1 + 4 * mul;
			num2 = num2 + 7 * mul;
		}
		mul = mul * 10; 
	}
	if(n-num1<=0 && num1>=num2) return num1;
	else return num2;
}


class BettingStrategy {
public:
	int finalSum(int, string);
};

int BettingStrategy::finalSum(int initSum, string outcome) {
	int bet = 1;
	for(int i = 0 ; i < outcome.size();i++)
	{
		if(initSum<bet) return initSum;
	
		if(outcome[i]=='W')
		{
			initSum = initSum + bet;
			bet = 1;
		}
		else
		{
			initSum = initSum - bet;
			bet = bet << 1;
		}
	}
	return initSum;
}


double test0() {
	int p0 = 12;
	string p1 = "WWWWWWWW";
	BettingStrategy * obj = new BettingStrategy();
	clock_t start = clock();
	int my_answer = obj->finalSum(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 20;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}

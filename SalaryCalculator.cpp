class SalaryCalculator {
public:
	double calcHours(int, int, int);
};

double SalaryCalculator::calcHours(int P1, int P2, int salary) {
	if(P1 * 200 >= salary)
	{
		return ((double)salary/P1);
	}
	else
	{
		double s1 = (double)((double)(P1)*200);
		double rem = salary - s1;
		double s2 = (double)((double)(rem)/P2);
		return 200.00 + s2;
	}
}



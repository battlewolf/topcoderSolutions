	int p0 = 48;
	SemiPerfectSquare * obj = new SemiPerfectSquare();
	clock_t start = clock();
	string my_answer = obj->check(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "Yes";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;

class SemiPerfectSquare {
public:
	string check(int);
};

string SemiPerfectSquare::check(int N) {
	for(int i = 1; i <= N >> 1; i++)
	{
		for(int j = i + 1; j <= N >> 1; j++)
		{
			if(i * (j * j) == N)
				return "Yes";
		}
	}
	return "No";
}



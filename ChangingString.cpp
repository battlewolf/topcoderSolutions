class ChangingString {
public:
	int distance(string, string, int);
};

int ChangingString::distance(string A, string B, int K) {
	vector<int> V(A.size(),0);
	for(int i = 0 ; i < A.size(); i++)
		V[i] = (int)(abs(A[i]-B[i]));
	sort(V.begin(), V.end());
	int s = 0;
	for(int i = V.size()-1 ; i >= 0;i--)
	{
		if(i>=(V.size()-K)){
			if(V[i]==0) V[i]=1;
			else V[i] = 0;
		}
		s = s + V[i];
	}
	
	
	return s;
}



class MultiRead {
public:
	int minCycles(string, int);
};

int MultiRead::minCycles(string trace, int procs) {
	int i,p,ans = 0,j,n=trace.size();
	while ( i < n )
	{
		if(trace[i] == 'W')
			ans++;
		else
		{
			for(j = 0 ; j < trace.size();j++)
				if(i + j >= n || trace[i+j] == 'W')
					break;
			ans++;
			i = i + j;	 
		}
	}
	return ans;
}



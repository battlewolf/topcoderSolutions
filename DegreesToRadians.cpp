class DegreesToRadians {
public:
	double convertToRadians(int, int, int);
};

double DegreesToRadians::convertToRadians(int degrees, int minutes, int seconds) {
	return ((degrees + minutes/60.00 + seconds/3600.00) * PI) / 180;	
}



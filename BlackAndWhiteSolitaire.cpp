class BlackAndWhiteSolitaire {
public:
	int minimumTurns(string);
};

int BlackAndWhiteSolitaire::minimumTurns(string s) {
	int m1 = 0, m2 = 0;
	bool v1 = 0, v2 = 1, v3;
	map<char, bool> M;
	M['W'] = 0;
	M['B'] = 1;
	for(int i = 0; i < s.size(); i++)
	{
		v3 = M[s[i]];
		v1 = 1 ^ v1;
		v2 = 1 ^ v2;
		if(v3 != v1) m1++;
		if(v3 != v2) m2++;
	}
	return min(m1, m2);
}


